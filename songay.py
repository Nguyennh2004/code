so_ngay = int(input("Nhập số ngày trong tuần: "))
if so_ngay == 1 :
    print("Monday")
elif so_ngay == 2 :
    print("Tuesday")
elif so_ngay == 3 :
    print("Wednesday")
elif so_ngay == 4 :
    print("Thursday")
elif so_ngay == 5 :
    print("Friday")
elif so_ngay == 6 :
    print("Saturday")
elif so_ngay == 7 :
    print("Sunday")
else :
    print("Không đúng")